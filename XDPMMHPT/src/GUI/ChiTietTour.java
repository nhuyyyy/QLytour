    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
        // TODO add your handling code here:
        ChiTietTourBUS bus = new ChiTietTourBUS();
        DiaDiemBUS ddbus = new DiaDiemBUS();
        int i = jt.getSelectedRow();
        if(i>=0){
           
            int result = JOptionPane.showConfirmDialog(null, "Xác nhận sửa ?","Xác nhận",JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION){
                DTO.ChiTietTour ctt = new DTO.ChiTietTour();
                ctt.setId(Integer.parseInt(txtID.getText()));
                ctt.setMatour(Integer.parseInt(txtMaTour.getText()));
                ctt.setMadiadiem(ddbus.getMaDiaDiem(cbDD.getSelectedItem().toString()));
                ctt.setThutu(Integer.parseInt(txtThuTu.getText()));
                
             
                int check = bus.sua(i, ctt);
                if(check == 1){
                    table.setValueAt(ctt.getId(), i, 0);
                    table.setValueAt(ctt.getMatour(), i, 1);
                    table.setValueAt(ddbus.getTenDiaDiem(ctt.getMadiadiem()), i, 2);
                    table.setValueAt(ctt.getThutu(), i, 3);
                    jt.setModel(table);
                    JOptionPane.showMessageDialog(null, "Sửa thành công");
                }
            }
        }
    }//GEN-LAST:event_btnSuaMouseClicked

