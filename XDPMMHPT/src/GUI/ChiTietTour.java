    private void doDuLieuLenBang() {
        
        ChiTietTourBUS bus = new ChiTietTourBUS();
        DiaDiemBUS ddbus = new DiaDiemBUS();
   
        try {
            ddbus.docDuLieu();
            bus.docDuLieu();
            table = (DefaultTableModel) jt.getModel();
            table.setRowCount(0);
            for(DTO.ChiTietTour ctt : bus.ds)
            {
                Vector vt = new Vector();
                vt.add(ctt.getId());
                vt.add(ctt.getMatour());
                vt.add(ddbus.getTenDiaDiem(ctt.getMadiadiem()));
                vt.add(ctt.getThutu());
                table.addRow(vt);
            }
            jt.setModel(table);
        } catch (Exception ex) {
            Logger.getLogger(Tour.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }

