    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        GiaTourBUS bus = new GiaTourBUS();
        txtMaGia.setText(bus.remindGia());
        DTO.GiaTour gt = new DTO.GiaTour();
        gt.setMagia(Integer.parseInt(txtMaGia.getText()));
        gt.setMatour(Integer.parseInt(txtMaTour.getText()));
        gt.setGiatien(Double.parseDouble(txtGia.getText()));
        System.out.println(jdcNgayBD.getDate());
        gt.setTgbd(jdcNgayBD.getDate());
        gt.setTgkt(jdcNgayKT.getDate());
        
        Vector head = new Vector();
        head.add(gt.getMagia());
        head.add(gt.getMatour());
        head.add((int)gt.getGiatien());
        DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        head.add(format.format(gt.getTgbd()));
        head.add(format.format(gt.getTgkt()));
      
        int rs = bus.them(gt);
        if(rs == 1){
                table.addRow(head);
                jt.setModel(table);
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            }else{
                JOptionPane.showMessageDialog(null, "Mã đã tồn tại. Thêm thất bại");
            }
    }//GEN-LAST:event_btnThemMouseClicked

    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        // TODO add your handling code here:
        int i = jt.getSelectedRow();
        if(i>=0){
            int result = JOptionPane.showConfirmDialog(null, "Xác nhận xóa ?", "Xác nhận",JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION){
                GiaTourBUS bus = new GiaTourBUS();
                int check = bus.xoa(i);  
                if(check == 1){
                    table.removeRow(i);
                    jt.setModel(table);
                    JOptionPane.showMessageDialog(null, "Xóa thành công");
                } 
            }
        }
    }//GEN-LAST:event_btnXoaMouseClicked

    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
        // TODO add your handling code here:
        GiaTourBUS bus = new GiaTourBUS();
        int i = jt.getSelectedRow();
        if(i>=0){
           
            int result = JOptionPane.showConfirmDialog(null, "Xác nhận sửa ?","Xác nhận",JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION){
                DTO.GiaTour gt = new DTO.GiaTour();
                gt.setMagia(Integer.parseInt(txtMaGia.getText()));
                gt.setMatour(Integer.parseInt(txtMaTour.getText()));
                gt.setGiatien(Double.parseDouble(txtGia.getText()));
                gt.setTgbd(jdcNgayBD.getDate());
                gt.setTgkt(jdcNgayKT.getDate());
                
                DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                Vector head = new Vector();
                head.add(gt.getMagia());
                head.add(gt.getMatour());
                head.add(gt.getGiatien());
                head.add(format.format(gt.getTgbd()));
                head.add(format.format(gt.getTgkt()));
                
                int check = bus.sua(i, gt);
                if(check == 1){
                    table.setValueAt(gt.getMatour(), i, 1);
                    table.setValueAt((int)gt.getGiatien(), i, 2);
                    table.setValueAt(format.format(gt.getTgbd()), i, 3);
                    table.setValueAt(format.format(gt.getTgkt()), i, 4);
                    jt.setModel(table);
                    JOptionPane.showMessageDialog(null, "Sửa thành công");
                }
            }
        }
    }//GEN-LAST:event_btnSuaMouseClicked

