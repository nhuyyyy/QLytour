    private void doDuLieuLenBang() {
        GiaTourBUS bus = new GiaTourBUS();
        try {
            bus.docDuLieu();
            table = (DefaultTableModel) jt.getModel();
            
            table.setRowCount(0);
            for(DTO.GiaTour gt : bus.ds)
            {
                Vector vt = new Vector();
                vt.add(gt.getMagia());
                vt.add(gt.getMatour());
                vt.add((int)gt.getGiatien());
                vt.add(gt.getTgbd());
                vt.add(gt.getTgkt());
                table.addRow(vt);
            }
            jt.setModel(table);
        } catch (Exception ex) {
            Logger.getLogger(Tour.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }

    private void setTextFields(String magia, String matour, String gia, String ngaybd, String ngaykt) {
        txtMaGia.setText(magia);
        txtMaTour.setText(matour);
        txtGia.setText(gia);
        try {
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            jdcNgayBD.setDate(dateFormat.parse(ngaybd));
            jdcNgayKT.setDate(dateFormat.parse(ngaykt));
        } catch (ParseException ex) {
            Logger.getLogger(GiaTour.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
