    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jdcDOB.getDate() == null || jdcDOB1.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Thời gian trống");
        } else {
            if ((jdcDOB.getDate()).compareTo(jdcDOB1.getDate()) <= 0) {
                ThongKeBUS tkBus = new ThongKeBUS();

                DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                String day_start = format.format(jdcDOB.getDate());
                String day_end = format.format(jdcDOB1.getDate());
                try {
                    tkBus.thongKeChung(day_start, day_end);
                    jLabel5.setText(tkBus.tk.getSoTour());
                    jLabel9.setText(tkBus.tk.getSoDoan());
                    jLabel12.setText(tkBus.tk.getSoKhach());
                } catch (Exception ex) {
                    Logger.getLogger(ThongKe.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ngày không hợp lệ");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jComboBox1.getSelectedItem().toString() != "Mã Tour") {
            System.out.println(jComboBox1.getSelectedItem().toString());
            ThongKeBUS tkBus = new ThongKeBUS();
            try {
                tkBus.thongKeChiPhi(jComboBox1.getSelectedItem().toString());
                System.out.println(tkBus.list_tk);
                jLabel16.setText("0");
                jLabel18.setText("0");
                jLabel20.setText("0");
                jLabel22.setText("0");
                for (DTO.ThongKe tk : tkBus.list_tk) {
                    String temp = tk.getSoDoan();
                    switch (temp) {
                        case "Xăng":
                        jLabel16.setText(tk.getSoTour());
                        break;
                        case "Khách Sạn":
                        jLabel18.setText(tk.getSoTour());
                        break;
                        case "Tham Quan":
                        jLabel20.setText(tk.getSoTour());
                        break;
                        case "Khác":
                        jLabel22.setText(tk.getSoTour());
                        break;
                        default:
                        System.out.println("0");
                    }
                }
                DecimalFormat formater = new DecimalFormat("###,###,###");
                float sum = Float.parseFloat(jLabel16.getText()) + Float.parseFloat(jLabel18.getText()) + Float.parseFloat(jLabel20.getText()) + Float.parseFloat(jLabel22.getText());
                jLabel23.setText(String.valueOf(formater.format(sum)) + " VND");
            } catch (Exception ex) {
                Logger.getLogger(ThongKe.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

