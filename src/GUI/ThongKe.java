    public ThongKe() {
        initComponents();
        getItem();
        ThongKeBUS tkBUS = new ThongKeBUS();
        try {
            float sum = 0;
            tkBUS.doanhThu();
            table = (DefaultTableModel) jTable2.getModel();
            table.setRowCount(0);
            for(DTO.ThongKe tk : tkBUS.list_tk)
            {
                Vector vt = new Vector();
                vt.add(tk.getSoDoan());
                vt.add(tk.getSoTour());
                vt.add(tk.getSoKhach());
                sum+=Float.parseFloat(tk.getSoKhach());
                vt.add(tk.getTen());
                table.addRow(vt);
            }
            jTable2.setModel(table);
            DecimalFormat formater = new DecimalFormat("###,###,###");
            jLabel27.setText(String.valueOf(formater.format(sum)) + " VND");
        } catch (Exception ex) {
            Logger.getLogger(ThongKe.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void getItem() {
        ThongKeBUS tkBus = new ThongKeBUS();
        int[] tmp = new int[100];
        tmp = tkBus.dsMaTour();
        for (int i = 0; i < tmp.length; i++) {
            if (tmp[i] == 0) {
                continue;
            }
            jComboBox1.addItem(String.valueOf(tmp[i]));
        }
    }

