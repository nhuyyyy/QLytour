    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked

        LoaiTourBUS bus = new LoaiTourBUS();
        txtMaLoaiTour.setText(bus.remindMaloaitour());
        DTO.LoaiTour loaitour = new DTO.LoaiTour();
        loaitour.setMaloaitour(Integer.parseInt(txtMaLoaiTour.getText()));
        loaitour.setTenloaitour(txtTenLoaiTour.getText());

        Vector head = new Vector();
        head.add(loaitour.getMaloaitour());
        head.add(loaitour.getTenloaitour());

        int rs = bus.them(loaitour);
        if(rs == 1){
            table.addRow(head);
            jt.setModel(table);
            JOptionPane.showMessageDialog(null, "Thêm thành công");
        }else{
            JOptionPane.showMessageDialog(null, "Mã đã tồn tại. Thêm thất bại");
        }
        cleanView();
    }//GEN-LAST:event_btnThemMouseClicked

